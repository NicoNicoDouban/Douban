$(".class1").click(function(){
	$(".right1").css({"position":"relative","top":"0px","float":"right"})
	$(this).prevAll().hide();
	$(this).nextAll().hide();
	if($(this).css("marginTop")=="44px"){
		$(this).animate({marginTop:'0px'},10);
	}
	$(".right1").css({"visibility":"visible","display":"block"});
})
